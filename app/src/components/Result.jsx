import React, { useState, useEffect } from 'react';

export default function Result({ reportId, onReset, onBack, onChat }) {
  const [data, setData] = useState({ tests: [], summary: "Loading analysis..." });
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchData = async () => {
      if (!reportId) return;
      try {
        const token = localStorage.getItem('token');
        const res = await fetch(`/api/reports/report/${reportId}`, {
          headers: { "Authorization": `Bearer ${token}` }
        });
        const json = await res.json();
        const payload = json.extracted_data || {};
        
        setData({
          tests: payload.tests || [],
          summary: json.summary || payload.summary || "No clinical summary available."
        });
      } catch (err) {
        console.error("Fetch error:", err);
      } finally {
        setLoading(false);
      }
    };
    fetchData();
  }, [reportId]);

  const handleDownload = () => {
    const content = `CLINICAL ANALYSIS SUMMARY\n\n${data.summary}`;
    const blob = new Blob([content], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `medical_summary_${reportId}.txt`;
    a.click();
  };

  if (loading) return <div className="dashboard-layout"><div className="spinner"></div></div>;

  return (
    <div className="dashboard-layout result-container">
      <header className="result-header">
        <h1>Clinical Analysis</h1>
        <div style={{ display: 'flex', gap: '10px' }}>
          {onBack && <button className="btn-secondary" onClick={onBack}>Back to Reports</button>}
          <button className="btn-primary" onClick={onReset}>Scan Another</button>
        </div>
      </header>

      <div className="glass-panel result-panel">
        <h2>Laboratory Test Results</h2>
        <table className="result-table">
          <thead>
            <tr>
              <th>Parameter</th>
              <th>Value</th>
              <th>Status</th>
            </tr>
          </thead>
          <tbody>
            {data.tests.map((t, i) => (
              <tr key={i}>
                <td style={{ fontWeight: 600, color: 'var(--text)' }}>{t.test_name}</td>
                <td>{t.value} {t.unit}</td>
                <td>
                  <span className={`status-badge ${t.status !== 'Normal' ? 'badge-abnormal' : 'badge-normal'}`}>{t.status}</span>
                </td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>

      <div className="glass-panel result-panel">
        <h2>Clinical Summary</h2>
        <p className="summary-text">{data.summary}</p>

        <div className="result-actions">
          {onChat && <button className="btn-primary" onClick={() => onChat(reportId)}>Ask AI</button>}
          <button className="btn-primary" onClick={handleDownload}>
            Download Summary
          </button>
          <p className="disclaimer">
            * Disclaimer: This summary is generated by AI. Please verify all clinical findings with a qualified medical professional before making healthcare decisions.
          </p>
        </div>
      </div>
    </div>
  );
}